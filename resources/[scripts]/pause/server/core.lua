local Tunnel = module("vrp", "lib/Tunnel")
local Proxy = module("vrp", "lib/Proxy")
vRP = Proxy.getInterface("vRP")
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONNECTION
-----------------------------------------------------------------------------------------------------------------------------------------
Expert = {}
Tunnel.bindInterface("pause", Expert)
-----------------------------------------------------------------------------------------------------------------------------------------
-- DISCONNECT
-----------------------------------------------------------------------------------------------------------------------------------------
function Expert.Disconnect()
    local source = source
    local Passport = vRP.Passport(source)
    if Passport then
		vRP.Kick(source,"Saiu da cidade.")
	end
end